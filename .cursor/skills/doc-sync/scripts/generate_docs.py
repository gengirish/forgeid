# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _read(path: Path) -> str:
    return path.read_text(encoding="utf-8", errors="replace") if path.exists() else ""


def _parse_ast(path: Path) -> ast.Module | None:
    src = _read(path)
    if not src:
        return None
    try:
        return ast.parse(src)
    except SyntaxError:
        return None


def _stamp() -> str:
    ts = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")
    return (
        f"> Auto-generated by `doc-sync` on {ts}.\n"
        f"> Do not edit — re-run `python .cursor/skills/doc-sync/scripts/generate_docs.py`.\n"
    )


