def generate_environment() -> str:
    lines = [f"# Environment Variables\n\n{_stamp()}\n"]

    config_path = BACKEND / "app" / "config.py"
    tree = _parse_ast(config_path)
    if tree:
        lines.append("## Backend (`backend/app/config.py` → `Settings`)\n\n")
        lines.append("| Variable | Type | Default | Purpose |\n")
        lines.append("|----------|------|---------|---------|\n")

        for node in ast.walk(tree):
            if not isinstance(node, ast.ClassDef) or node.name != "Settings":
                continue
            for item in node.body:
                if not isinstance(item, ast.AnnAssign) or not item.target:
                    continue
                name = ast.unparse(item.target)
                ann = ast.unparse(item.annotation) if item.annotation else ""
                default = ast.unparse(item.value) if item.value else "—"
                env_name = name.upper()
                purpose = _env_purpose(name)
                lines.append(f"| `{env_name}` | `{ann}` | `{default}` | {purpose} |\n")
        lines.append("\n")

    lines.append("## Frontend\n\n")
    lines.append("| Variable | Default | Purpose |\n")
    lines.append("|----------|---------|---------|\n")
    lines.append("| `NEXT_PUBLIC_API_URL` | `http://localhost:8000` | Backend API base URL |\n\n")

    lines.append("## CI/CD Secrets (GitHub Actions)\n\n")
    lines.append("| Secret | Used By |\n|--------|---------|\n")
    lines.append("| `FLY_API_TOKEN` | `deploy.yml` — Fly.io backend deploy |\n")
    lines.append("| `VERCEL_TOKEN` | `deploy.yml` — Vercel frontend deploy |\n")
    lines.append("| `VERCEL_ORG_ID` | `deploy.yml` — Vercel org scope |\n")
    lines.append("| `VERCEL_PROJECT_ID` | `deploy.yml` — Vercel project |\n\n")

    env_example = ROOT / ".env.example"
    if env_example.exists():
        lines.append("## `.env.example`\n\n```env\n")
        lines.append(_read(env_example))
        lines.append("```\n")

    return "".join(lines)


def _env_purpose(name: str) -> str:
    purposes = {
        "app_name": "Application display name",
        "api_secret_key": "Secret for JWT / session signing",
        "debug": "Enable debug mode",
        "database_url": "PostgreSQL connection string (asyncpg)",
        "redis_url": "Redis connection string",
        "cors_origins": "Comma-separated allowed CORS origins",
        "openai_api_key": "OpenAI API key for scanner targets",
        "anthropic_api_key": "Anthropic API key for scanner targets",
        "azure_openai_api_key": "Azure OpenAI key",
        "azure_openai_endpoint": "Azure OpenAI endpoint URL",
        "hf_token": "Hugging Face token",
    }
    return purposes.get(name, "—")


