def main():
    DOCS.mkdir(exist_ok=True)

    generators = {
        "API_REFERENCE.md": generate_api_reference,
        "DATA_MODELS.md": generate_data_models,
        "ARCHITECTURE.md": generate_architecture,
        "ENVIRONMENT.md": generate_environment,
        "DEPLOYMENT.md": generate_deployment,
        "DEVELOPMENT.md": generate_development,
    }

    for filename, gen_fn in generators.items():
        path = DOCS / filename
        content = gen_fn()
        path.write_text(content, encoding="utf-8")
        print(f"  wrote {path.relative_to(ROOT)}")

    print(f"\nDocs generated: {len(generators)} files in docs/")


if __name__ == "__main__":
    main()
