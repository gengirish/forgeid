    lines.append("## Testing\n\n```bash\ncd backend\nuv run pytest          # run all tests\nuv run pytest -v      # verbose\n```\n\n")

    lines.append("## Linting\n\n```bash\ncd backend\nuv run ruff check .    # check\nuv run ruff check . --fix  # auto-fix\n```\n\n")

    lines.append("## Scripts\n\n")
    scripts_dir = ROOT / "scripts"
    if scripts_dir.exists():
        lines.append("| Script | Purpose |\n|--------|---------|\n")
        for py in sorted(scripts_dir.glob("*.py")):
            doc = ""
            tree = _parse_ast(py)
            if tree and ast.get_docstring(tree):
                doc = ast.get_docstring(tree).split("\n")[0]
            lines.append(f"| `scripts/{py.name}` | {doc or py.stem} |\n")
        lines.append("\n")

    lines.append("## Project Conventions\n\n")
    lines.append("- Backend uses `uv` (not pip) — `pyproject.toml` + `uv.lock`\n")
    lines.append("- All API routes are under `/api/v1/`\n")
    lines.append("- ORM models in `backend/app/models/`, Pydantic schemas in `backend/app/schemas/`\n")
    lines.append("- Frontend uses App Router (Next.js 15)\n")
    lines.append("- Tailwind CSS 4 + shadcn/ui for styling\n")

    return "".join(lines)


