def _extract_columns(models_dir: Path) -> dict[str, list[dict]]:
    result: dict[str, list[dict]] = {}
    for py in sorted(models_dir.glob("*.py")):
        if py.name.startswith("_"):
            continue
        tree = _parse_ast(py)
        if not tree:
            continue
        for node in ast.walk(tree):
            if not isinstance(node, ast.ClassDef):
                continue
            bases = [ast.unparse(b) for b in node.bases]
            if not any("Base" in b for b in bases):
                continue
            cols = []
            tablename = ""
            for item in node.body:
                if isinstance(item, ast.Assign):
                    for t in item.targets:
                        if isinstance(t, ast.Name) and t.id == "__tablename__":
                            tablename = ast.unparse(item.value).strip("'\"")
                if isinstance(item, ast.AnnAssign) and item.target:
                    name = ast.unparse(item.target)
                    ann = ast.unparse(item.annotation) if item.annotation else ""
                    cols.append({"name": name, "type": ann})
            result[node.name] = cols
            if tablename:
                result[node.name].insert(0, {"name": "__tablename__", "type": tablename})
    return result


def _extract_schemas(schemas_dir: Path) -> dict[str, list[dict]]:
    result: dict[str, list[dict]] = {}
    for py in sorted(schemas_dir.glob("*.py")):
        if py.name.startswith("_"):
            continue
        tree = _parse_ast(py)
        if not tree:
            continue
        for node in ast.walk(tree):
            if not isinstance(node, ast.ClassDef):
                continue
            fields = []
            for item in node.body:
                if isinstance(item, ast.AnnAssign) and item.target:
                    name = ast.unparse(item.target)
                    ann = ast.unparse(item.annotation) if item.annotation else ""
                    default = ""
                    if item.value:
                        default = ast.unparse(item.value)
                    fields.append({"name": name, "type": ann, "default": default})
            if fields:
                result[node.name] = fields
    return result


def generate_data_models() -> str:
    lines = [f"# Data Models\n\n{_stamp()}\n"]

    models = _extract_columns(BACKEND / "app" / "models")
    schemas = _extract_schemas(BACKEND / "app" / "schemas")

    lines.append("## ORM Models (SQLAlchemy)\n\n")
    for cls, cols in models.items():
        table = ""
        display_cols = []
        for c in cols:
            if c["name"] == "__tablename__":
                table = c["type"]
            else:
                display_cols.append(c)
        header = f"### `{cls}`" + (f" — table `{table}`" if table else "")
        lines.append(f"{header}\n\n")
        lines.append("| Column | Type |\n|--------|------|\n")
        for c in display_cols:
            lines.append(f"| `{c['name']}` | `{c['type']}` |\n")
        lines.append("\n")

    lines.append("## Pydantic Schemas\n\n")
    for cls, fields in schemas.items():
        lines.append(f"### `{cls}`\n\n")
        lines.append("| Field | Type | Default |\n|-------|------|---------|\n")
        for f in fields:
            d = f"`{f['default']}`" if f["default"] else "—"
            lines.append(f"| `{f['name']}` | `{f['type']}` | {d} |\n")
        lines.append("\n")

    return "".join(lines)


