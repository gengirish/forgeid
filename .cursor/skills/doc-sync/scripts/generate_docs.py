def generate_architecture() -> str:
    lines = [f"# Architecture\n\n{_stamp()}\n"]

    lines.append("## System Overview\n\n")
    lines.append("```\n")
    lines.append("┌─────────────────┐     ┌─────────────────┐     ┌──────────────┐\n")
    lines.append("│  Next.js 15     │────▶│  FastAPI         │────▶│  Neon        │\n")
    lines.append("│  (Vercel)       │     │  (Fly.io)        │     │  PostgreSQL  │\n")
    lines.append("└─────────────────┘     └────────┬─────────┘     └──────────────┘\n")
    lines.append("                                 │\n")
    lines.append("                        ┌────────▼─────────┐\n")
    lines.append("                        │  Upstash Redis   │\n")
    lines.append("                        │  (Fly.io)        │\n")
    lines.append("                        └──────────────────┘\n")
    lines.append("```\n\n")

    lines.append("## Directory Structure\n\n```\n")
    for dirpath, dirnames, filenames in os.walk(ROOT):
        dirnames[:] = [d for d in dirnames if d not in {
            "node_modules", ".next", "__pycache__", ".git", ".venv",
            "venv", ".cursor", ".vercel", "dist",
        }]
        rel = os.path.relpath(dirpath, ROOT)
        depth = rel.count(os.sep) if rel != "." else 0
        indent = "  " * depth
        folder = os.path.basename(dirpath) if rel != "." else "TrustGate/"
        lines.append(f"{indent}{folder}/\n")
        for f in sorted(filenames):
            if f.startswith(".") and f not in {".env.example", ".dockerignore", ".gitignore"}:
                continue
            lines.append(f"{indent}  {f}\n")
    lines.append("```\n\n")

    lines.append("## Data Flow\n\n")
    lines.append("1. User triggers scan via Dashboard or API\n")
    lines.append("2. Backend creates `Scan` record (status=pending)\n")
    lines.append("3. Background task runs garak against the `Target` model\n")
    lines.append("4. Results are normalized into `Finding` records with OWASP mapping\n")
    lines.append("5. Safety score is computed and `Scan` record updated\n")
    lines.append("6. Dashboard polls for results and renders charts\n\n")

    lines.append("## Tech Stack\n\n")
    lines.append("| Layer | Technology | Deployed To |\n")
    lines.append("|-------|-----------|-------------|\n")

    pkg_json = FRONTEND / "package.json"
    fe_deps = ""
    if pkg_json.exists():
        pj = json.loads(_read(pkg_json))
        fe_deps = ", ".join(sorted(pj.get("dependencies", {}).keys()))

    pyproject = BACKEND / "pyproject.toml"
    be_deps = ""
    if pyproject.exists():
        content = _read(pyproject)
        m = re.findall(r'"([a-zA-Z][a-zA-Z0-9_-]*)', content)
        be_deps = ", ".join(sorted(set(m)))

    lines.append(f"| Frontend | Next.js, React, Tailwind, shadcn/ui | Vercel |\n")
    lines.append(f"| Backend  | FastAPI, SQLAlchemy, Pydantic, uvicorn | Fly.io |\n")
    lines.append(f"| Database | PostgreSQL (Neon serverless) | Neon |\n")
    lines.append(f"| Cache    | Redis (Upstash) | Fly.io |\n")
    lines.append(f"| Scanner  | garak v0.14+ | Local / CI |\n")
    lines.append(f"| CI/CD    | GitHub Actions | GitHub |\n\n")

    return "".join(lines)


