    lines = [f"# Development Guide\n\n{_stamp()}\n"]

    lines.append("## Prerequisites\n\n")
    lines.append("- Python 3.12+\n- Node.js 18+\n- Docker & Docker Compose\n- `uv` (Python package manager)\n\n")

    lines.append("## Local Setup\n\n")
    lines.append("```bash\n")
