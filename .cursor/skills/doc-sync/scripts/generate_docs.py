_HTTP_METHODS = {"get", "post", "put", "patch", "delete", "head", "options"}


def _extract_routes(api_dir: Path) -> list[dict]:
    routes: list[dict] = []
    for py in sorted(api_dir.glob("*.py")):
        if py.name.startswith("_"):
            continue
        tree = _parse_ast(py)
        if not tree:
            continue
        tag = py.stem
        for node in ast.walk(tree):
            if not isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef)):
                continue
            for deco in node.decorator_list:
                method = path_str = ""
                response_model = ""
                status_code = ""
                if isinstance(deco, ast.Attribute) and deco.attr in _HTTP_METHODS:
                    method = deco.attr.upper()
                elif isinstance(deco, ast.Call):
                    func = deco.func
                    if isinstance(func, ast.Attribute) and func.attr in _HTTP_METHODS:
                        method = func.attr.upper()
                        for arg in deco.args:
                            if isinstance(arg, ast.Constant):
                                path_str = arg.value
                        for kw in deco.keywords:
                            if kw.arg == "response_model":
                                response_model = ast.unparse(kw.value)
                            if kw.arg == "status_code":
                                status_code = ast.unparse(kw.value)
                if not method:
                    continue

                params = []
                for a in node.args.args:
                    if a.arg in ("self", "db", "background_tasks"):
                        continue
                    ann = ast.unparse(a.annotation) if a.annotation else ""
                    params.append(f"{a.arg}: {ann}" if ann else a.arg)

                routes.append({
                    "tag": tag,
                    "method": method,
                    "path": path_str or "/",
                    "name": node.name,
                    "params": params,
                    "response_model": response_model,
                    "status_code": status_code,
                    "line": node.lineno,
                    "file": py.name,
                })
    return routes


def _prefix_for_tag(tag: str) -> str:
    mapping = {
        "projects": "/api/v1/projects",
        "targets": "/api/v1/targets",
        "scans": "/api/v1/scans",
        "findings": "/api/v1/findings",
    }
    return mapping.get(tag, f"/api/v1/{tag}")


def generate_api_reference() -> str:
    lines = [f"# API Reference\n\n{_stamp()}\n"]
    lines.append("Base URL: `https://trustgate-api-prod.fly.dev` (production) "
                  "| `http://localhost:8000` (local)\n\n")
    lines.append("## Health Check\n\n| Method | Path | Response |\n|---|---|---|\n"
                 "| GET | `/health` | `{\"status\":\"ok\",\"version\":\"0.1.0\"}` |\n\n")

    api_dir = BACKEND / "app" / "api"
    routes = _extract_routes(api_dir)
    if not routes:
        lines.append("_No routes detected._\n")
        return "\n".join(lines)

    tags = sorted({r["tag"] for r in routes})
    for tag in tags:
        prefix = _prefix_for_tag(tag)
        lines.append(f"## {tag.title()}\n")
        lines.append("| Method | Endpoint | Handler | Response Model | Status |\n")
        lines.append("|--------|----------|---------|----------------|--------|\n")
        for r in routes:
            if r["tag"] != tag:
                continue
            full = f"`{prefix}{r['path']}`"
            rm = f"`{r['response_model']}`" if r["response_model"] else "—"
            sc = r["status_code"] or "200"
            lines.append(f"| {r['method']} | {full} | `{r['name']}` | {rm} | {sc} |\n")
        lines.append("\n")

        for r in routes:
            if r["tag"] != tag:
                continue
            if r["params"]:
                lines.append(f"### `{r['method']} {prefix}{r['path']}`\n\n")
                lines.append("**Parameters:**\n\n")
                for p in r["params"]:
                    lines.append(f"- `{p}`\n")
                lines.append("\n")

    return "".join(lines)


